var num = 29;

if (num >= 80 && num <= 100) {
	
	document.write("A+");
}


else if (num >= 70) {
	
	document.write("A");
}

else if (num >= 65) {
	
	document.write("A-");
}

else if (num >= 60) {
	
	document.write("B");
}

else if (num >= 55) {
	
	document.write("B-");
}

else if (num >= 50) {
	
	document.write("C");
}

else if (num >= 40) {
	
	document.write("D");
}

else {
	
	document.write("Fail!!!");
}